    public boolean authenticateUser(String username,char passwd[]){
        
    PreparedStatement ps = null;
    ResultSet resset=null; 
    Boolean rslt = false;

                String loginQuery = "SELECT * FROM Users WHERE Username =? AND Password = ?";

                try {
                    ps = database.getConnection().prepareStatement(loginQuery);
                    ps.setString(1, username);
                    ps.setBytes(2, Utils.makePasswordDigest(username, passwd));
                 
                    ps.execute(); 
                    
                    resset = ps.getResultSet();
                     
                    rslt = resset.next();

                } catch (Exception e) 
                {
                    JOptionPane.showMessageDialog(LoginPanel, e, "Login Exception", JOptionPane.ERROR_MESSAGE);
        
                } 
            
           return rslt;
    }//end of authenticate user
    
